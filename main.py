"""
This module provides basic mathematical operations: addition, subtraction, division, and multiplication.
"""

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def division(a, b):
    return a / b

def multiplication(a, b):
    return a * b

# Test the functions
print(add(6, 2))
print(subtract(6, 2))
print(division(6, 2))
print(multiplication(6, 2))
